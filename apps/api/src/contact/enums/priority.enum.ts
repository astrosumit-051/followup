/**
 * Contact Priority Levels
 *
 * Defines the importance/frequency of engagement with a contact:
 * - HIGH: Key relationships requiring frequent contact (weekly/bi-weekly)
 * - MEDIUM: Important contacts requiring regular contact (monthly)
 * - LOW: Occasional contacts requiring infrequent contact (quarterly/semi-annually)
 */
export enum Priority {
  HIGH = 'HIGH',
  MEDIUM = 'MEDIUM',
  LOW = 'LOW',
}
